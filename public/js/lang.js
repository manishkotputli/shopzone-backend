function getLang() { return localStorage.getItem('sz_lang') || 'en'; }
function setLang(l) { localStorage.setItem('sz_lang', l); applyLang(); }
function t(key) { return (LANG[getLang()] || LANG.en)[key] || key; }

function applyLang() {
  document.querySelectorAll('[data-t]').forEach(el => {
    const key = el.getAttribute('data-t');
    if (key) el.textContent = t(key);
  });
  document.querySelectorAll('[data-t-ph]').forEach(el => {
    const key = el.getAttribute('data-t-ph');
    if (key) el.placeholder = t(key);
  });
  // Update lang toggle btn
  const btn = document.getElementById('langToggle');
  if (btn) btn.textContent = getLang() === 'en' ? 'हिंदी' : 'English';
}

document.addEventListener('DOMContentLoaded', applyLang);
